#include <stdio.h>

int main()
{
    char ch = 'A';
    int num = 100;
    float fnum = 12.2;
    double dnum = 23.25;

    printf("%c\n", ch);     // Print character
    printf("%d\n", num);    // Print integer
    printf("%f\n", fnum);   // Print float
    printf("%lf\n", dnum);  // Print double

    return 0; // terminate program
}
